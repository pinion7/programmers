function solution(str) {
  // 분석
  // 문자열이 사칙연산과 조합시 어떤 결과가 나오는지
  // 체크하고 이해해보라는 안배가 숨어있는 문제다.
  
  // 풀이 1
  // 일단 그냥 문자열 -> 숫자 변환 함수 쓰면 된다.
  // ex) return Number(str);
  
  // 풀이 2
  // 숫자형태의 문자열은 곱셈, 나눗셈, 뺄셈과 조합시, 연산 수행과 동시에 숫자타입으로 변환된다.
  // 반면 덧셈이랑 조합하면 그대로 뒤에 붙는다. 즉, 문자열타입은 덧셈 연산만 가능한 셈이다.
  return str * 1;
}